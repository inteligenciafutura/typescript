// Declaramos una variable con tipo string
var nombre = "Javier";
// Declaramos una variable con tipo number
var edad = 28;
// Declaramos una variable con tipo boolean
var esMayorDeEdad = edad >= 18;
// Declaramos una variable con tipo null
var valorNull = null;
// Declaramos una variable con tipo undefined
var valorUndefined = undefined;
// Declaramos una variable con tipo any
var valorAny = "Javier";
// Imprimimos las variables por pantalla
console.log(nombre); // Javier
console.log(edad); // 28
console.log(esMayorDeEdad); // true
console.log(valorNull); // null
console.log(valorUndefined); // undefined
console.log(valorAny); // Javier
